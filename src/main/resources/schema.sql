CREATE TABLE IF NOT EXISTS USERS
(
    USER_ID INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NAME    VARCHAR(50)         NOT NULL,
    EMAIL   VARCHAR(100) UNIQUE NOT NULL
);

CREATE TABLE IF NOT EXISTS USER_RESULTS
(
    RESULT_ID             INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    CORRECT_CLICKS        INTEGER NOT NULL,
    INCORRECT_CLICKS      INTEGER NOT NULL,
    OMISSIONS             INTEGER NOT NULL,
    AVERAGE_REACTION_TIME DOUBLE  NOT NULL,
    USER_ID               INTEGER REFERENCES USERS (USER_ID)
);

CREATE TABLE IF NOT EXISTS COGNITIVE_STYLE_DESCRIPTION
(
    id          INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    STYLE       VARCHAR(50)   NOT NULL,
    DESCRIPTION VARCHAR(1000) NOT NULL
);

